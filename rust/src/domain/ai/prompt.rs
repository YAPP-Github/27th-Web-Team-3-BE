/// 회고 가이드 프롬프트 생성
pub fn create_retrospective_guide_prompt(content: &str) -> String {
    format!(
        r#"당신은 회고 작성을 돕는 전문 코치입니다.

사용자가 작성 중인 회고 내용:
```
{}
```

위 내용을 분석하여 다음 관점에서 조언해주세요:
1. 구체성: 더 구체적으로 작성할 부분이 있는지
2. 감정 표현: 당시 느낌이나 생각을 더 표현하면 좋을지
3. 학습 포인트: 배운 점이나 개선할 점을 명확히 할 수 있는지
4. 다음 액션: 앞으로 어떻게 할지 계획을 추가하면 좋을지

친근하고 격려하는 톤으로 2-3문장으로 간단히 조언해주세요."#,
        content
    )
}

/// 회고 다듬기 프롬프트 생성
pub fn create_refine_prompt(content: &str, tone_style: &str) -> String {
    let tone_description = match tone_style {
        "상냥체" => "상냥하고 친근한 말투",
        "정중체" => "정중하고 격식있는 말투",
        _ => "자연스러운 말투",
    };

    format!(
        r#"당신은 회고 내용을 다듬어주는 전문 편집자입니다.

원본 회고 내용:
```
{}
```

위 내용을 다음 기준으로 다듬어주세요:
1. 비속어나 은어를 적절한 표현으로 변경
2. {}로 자연스럽게 재작성
3. 의미는 유지하되 더 명확하고 읽기 좋게 개선
4. 회고의 본질적 의미와 감정은 보존

다듬어진 내용만 출력해주세요."#,
        content, tone_description
    )
}

